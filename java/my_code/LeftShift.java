public class Arithmetic
{
    public static void main(String args[])
    {
        System.out.println(10 << 2); /* 10 x 2^2 = 40 */
        System.out.println(10 << 3); /* 10 x 2^3 = 80 */
        System.out.println(20 << 2); /* 20 x 2^2 = 80 */
        System.out.println(15 << 4); /* 15 x 2^4 = 240 */
        System.out.println(40 << 5); /* 40 x 2^5 = 1,280 */
    }
}